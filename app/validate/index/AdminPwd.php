declare (strict_types=1);

namespace app\validate\index;

use think\Validate;

class AdminPwd extends Validate
 {
protected $rule = [
         'password' => ['min' => 6, 'max' => 12, 'regex' => '/^(?![0-9]+$)(?![a-z]+$)(?![A-Z]+$)(?!([^(0-9a-zA-Z)]|[\(\)])+$)([^(0-9a-zA-Z)]|[\(\)]|[a-z]|[A-Z]|[0-9]){6,}$/
'],
    ];

    protected $message = [
        'password.min' => '密码不能小于6个字符',
        'password.max' => '密码最多不能超过12个字符',
        'password.regex' => '密码必须包含大小写字母/数字/符号任意两者组合',
    ];

}